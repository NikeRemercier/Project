    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $katakunci = $request->katakunci;
        $jumlahbaris = 5;
        if(strlen($katakunci)){
            $data = view_join_pemeliharaan_barang::where('kode_barang','like',"%$katakunci%")
            ->orWhere('nama_user','like',"%$katakunci%")
            ->paginate($jumlahbaris);
        }else{
            $data = view_join_pemeliharaan_barang::orderBy('id_peminjam', 'desc')->paginate($jumlahbaris);
        }
        return view('pemeliharaan_barang.index')->with('pemeliharaan_barang', $data);
    }

